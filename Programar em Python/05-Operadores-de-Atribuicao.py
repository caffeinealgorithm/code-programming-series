x = 2

x += 3
print('x = x + 3:', x) # x = x + 3: 5

x -= 2
print('x = x - 2:', x) # x = x - 2: 3

x *= 2
print('x = x * 2:', x) # x = x * 2: 6

x /= 2
print('x = x / 2:', x) # x = x / 2: 3

x %= 3
print('x = x % 3:', x) # x = x % 3: 0

x **= 2
print('x = x ** 2:', x) # x = x ** 2: 0
