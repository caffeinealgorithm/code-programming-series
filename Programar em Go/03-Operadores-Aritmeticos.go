package main

import "fmt"

func main() {
	fmt.Println("Adição:", (10 + 10)) // Adição: 20
	fmt.Println("Subtração:", (10.5 - 5.5 - 4.5)) // Subtração: 0.5
	fmt.Println("Multiplicação:", (-10 * 2)) // Multiplicação: -20
	fmt.Println("Divisão:", (10 / 10)) // Divisão: 1.0
	fmt.Println("Resto:", (8 % 2)) // Resto: 0
}
